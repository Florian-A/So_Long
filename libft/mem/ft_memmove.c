#include "../includes/libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	if (!((char *)dst) && !((char *)src))
		return (0);
	if (dst > src)
	{
		while (len > 0)
		{
			((char *)dst)[len - 1] = ((char *)src)[len - 1];
			len--;
		}
	}
	else
		ft_memcpy(((char *)dst), ((char *)src), len);
	return (((char *)dst));
}
