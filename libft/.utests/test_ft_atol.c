#include <stdio.h>
#include "../includes/utest.h"

int	test_ft_atol(void)
{
	char	*s1;

	s1 = "";
	if (ft_atol(s1) != 0)
		return (-1);
	s1 = "9223372036854775807";
	if (ft_atol(s1) != 9223372036854775807)
		return (-1);
	s1 = "-9223372036854775807";
	if (ft_atol(s1) != -9223372036854775807)
		return (-1);
	return (0);
}
