int	putu_fd(int n, int fd)
{
	int				i;
	unsigned int	n1;

	i = 1;
	n1 = n;
	ft_putunbr_fd(n, fd);
	while (n1 > 9)
	{
		n1 = n1 / 10;
		i++;
	}
	return (i);
}
