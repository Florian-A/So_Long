#include "../includes/libft.h"

int	putp_fd(unsigned long long pnt, int fd)
{
	int		i;
	char	*s;

	s = ull_to_hex(pnt);
	i = puts_fd("0x", fd);
	i += puts_fd(s, fd) - 1;
	free(s);
	return (i);
}
