#include "../includes/libft.h"

int	ft_isnbr(char *nbr)
{
	int	i;
	int	len;

	len = ft_strlen(nbr);
	if (nbr[0] == '-' || nbr[0] == '+')
		i = 1;
	else
		i = 0;
	while (i < len)
	{
		if (ft_isdigit(nbr[i]))
			i++;
		else
			return (0);
	}
	return (1);
}
